use Symfony\Component\EventDispatcher\Event;

class GetDataEvent extends Event
{
    protected $data;

    public function setData(array $data)
    {
        $this->data = $data;
    }

    /**
     * @return array
     */
    public function getData()
    {
        return $this->data;
    }
}
